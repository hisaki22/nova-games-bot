export function pickRound(): { category: Category; word: string } {
  const category = categories[Math.floor(Math.random() * categories.length)]!;
  const word = category.words[Math.floor(Math.random() * category.words.length)]!;
  return { category, word };
}

export function pickEightOptions(category: Category, secret: string): string[] {
  const pool = category.words.filter((w) => w !== secret);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  const options = [secret, ...shuffled.slice(0, 7)];
  return options.sort(() => Math.random() - 0.5);
}
